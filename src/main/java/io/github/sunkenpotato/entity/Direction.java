package io.github.sunkenpotato.entity;

/**
 * <h2>Direction Enum</h2>
 *
 * This Enum (Direction) represents all 4 direction states possible to an
 * {@link io.github.sunkenpotato.entity.Entity}:
 * <ul>
 *     <li>Up</li>
 *     <li>Down</li>
 *     <li>Left</li>
 *     <li>Right</li>
 * </ul>
 */
public enum Direction {
    Up,
    Down,
    Left,
    Right
}
